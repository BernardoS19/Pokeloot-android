package com.example.pokeloot_android.modelos;

public class Baralho {
    private int id;
    private String nome;

    public Baralho(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

}
